public class Calculator {

    public int add (int num1, int num2){
        return  num1 + num2;
    }

    public int subtract (int base, int subtractAmount){
        return base - subtractAmount;
    }

    public int multiply (int base, int multiplyBy){
        return  base * multiplyBy;
    }

    public int divide (int base, int dividedBy){
        return  base/dividedBy;
    }

}
